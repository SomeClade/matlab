% Построение треугольной функции принадлежности
x = 3:0.1:7; % Диапазон значений x
mu = max(0, 1 - abs(x - 5) / 2); % Треугольная функция
plot(x, mu, 'LineWidth', 2); % Построение графика
grid on;
title('Функция принадлежности'); % Заголовок графика
xlabel('x'); % Подпись оси x
ylabel('μ(x)'); % Подпись оси y
