% Задача 11: Нечеткие числа
a = [1/4, 0.5/5, 0.2/6];
b = [0.3/1, 0.8/2, 0.8/3, 0.4/4];

% Сложение и умножение
sum_ab = a + b(1:length(a)); % Элементное сложение
prod_ab = a .* b(1:length(a)); % Элементное умножение

% Результаты
disp('Сумма a + b:');
disp(sum_ab);
disp('Произведение a * b:');
disp(prod_ab);
